        public bool MoveServo(ServosEnum servo, int position, int speed = -1)
        {
            if (this._robot.IsConnected)
            {
                ////Because of servos movement limitation and tuning
                //if (position > 175)
                //{
                //    position = 175;
                //}
                //else if (position < 0)
                //{
                //    position = 5;
                //}

                if (speed != -1)
                {
                    this._robot.Servo.SetServoPosition(servo.ToServoPortEnum(), position, speed);
                }
                else
                {
                    this._robot.Servo.SetServoPosition(servo.ToServoPortEnum(), position, Movement.MIN_SPEED);
                }

                return true;
            }
            else
            {
                return false;
            }
        }

        public bool MoveServo(ServosEnum servo, int x, int y, int z, int speed = -1)
        {
            int adc = this._robot.ADC.GetADCValue(ADC.ADCPortEnum.ADC0);
            int servoPosition = Convert.ToInt16(Functions.GetScalarFromRange(x, y, z) * adc);
            this.MoveServo(servo, servoPosition, speed);
            return true;
        }

